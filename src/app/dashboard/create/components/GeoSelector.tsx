export default function GeoSelector({ selectedCountries, onSelect }: GeoSelectorProps) {
  const [isOpen, setIsOpen] = useState(false)
  const [searchTerm, setSearchTerm] = useState('')
  const [userPlan, setUserPlan] = useState<'free' | 'unlimited'>('free')
  const [loading, setLoading] = useState(true)
  const dropdownRef = useRef<HTMLDivElement>(null)

  useEffect(() => {
    const fetchUserPlan = async () => {
      try {
        const supabase = createClient()
        const { data: { user } } = await supabase.auth.getUser()
        
        if (user) {
          const plan = await getUserPlan(user.id)
          setUserPlan(plan)
        }
      } catch (error) {
        console.warn('Error fetching user plan:', error)
      } finally {
        setLoading(false)
      }
    }

    fetchUserPlan()
  }, [])

  // Handle click outside to close dropdown
  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target as Node)) {
        setIsOpen(false)
      }
    }

    document.addEventListener('mousedown', handleClickOutside)
    return () => document.removeEventListener('mousedown', handleClickOutside)
  }, [])

  const filteredCountries = countries.filter(country =>
    country.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
    country.code.toLowerCase().includes(searchTerm.toLowerCase())
  )

  const handleCountrySelect = (countryCode: string) => {
    if (selectedCountries.includes(countryCode)) {
      onSelect(selectedCountries.filter(code => code !== countryCode))
    } else {
      onSelect([...selectedCountries, countryCode])
    }
  }

  const handleRemoveCountry = (countryCode: string) => {
    onSelect(selectedCountries.filter(code => code !== countryCode))
  }

  if (loading) {
    return (
      <div className="space-y-3">
        <label className="block text-sm font-medium text-gray-900">
          Geo Targeting
        </label>
        <div className="h-10 bg-gray-100 rounded-lg animate-pulse" />
      </div>
    )
  }

  return (
    <div className="space-y-3" ref={dropdownRef}>
      <div className="flex items-center justify-between">
        <p className="text-sm text-gray-600">
          Show your announcement only to visitors from specific countries
        </p>
      </div>

      {/* Selected countries display */}
      {selectedCountries.length > 0 && (
        <div className="flex flex-wrap gap-2 mb-2">
          {selectedCountries.map(code => {
            const country = countries.find(c => c.code === code)
            if (!country) return null
            
            return (
              <span
                key={code}
                className="inline-flex items-center bg-gray-100 text-gray-800 text-sm px-3 py-1 rounded-full"
              >
                {country.name}
                <button
                  onClick={() => handleRemoveCountry(code)}
                  className="ml-2 text-gray-500 hover:text-gray-700"
                >
                  <X className="w-3 h-3" />
                </button>
              </span>
            )
          })}
        </div>
      )}

      {/* Dropdown button */}
      <div className="relative">
        <button
          type="button"
          onClick={() => setIsOpen(!isOpen)}
          disabled={userPlan === 'free'}
          className={`w-full flex items-center justify-between px-3 py-2 border rounded-lg shadow-sm text-left ${
            userPlan === 'free'
              ? 'bg-gray-50 border-gray-200 text-gray-500 cursor-not-allowed'
              : 'bg-white border-gray-300 text-gray-900 hover:bg-gray-50'
          }`}
        >
          <span className="block truncate">
            {selectedCountries.length > 0
              ? `${selectedCountries.length} ${selectedCountries.length === 1 ? 'country' : 'countries'} selected`
              : 'Select countries'}
          </span>
          <ChevronDown className={`w-4 h-4 transition-transform ${isOpen ? 'transform rotate-180' : ''}`} />
        </button>

        {/* Dropdown menu */}
        {isOpen && (
          <div className="absolute z-50 w-full mt-1 bg-white border border-gray-200 rounded-lg shadow-lg">
            {/* Search input */}
            <div className="p-2 border-b border-gray-200">
              <div className="relative">
                <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 w-4 h-4 text-gray-400" />
                <input
                  type="text"
                  className="w-full pl-9 pr-3 py-2 border border-gray-200 rounded-md text-sm focus:outline-none focus:ring-2 focus:ring-brand-500 focus:border-transparent placeholder:text-gray-500"
                  placeholder="Search countries..."
                  value={searchTerm}
                  onChange={(e) => setSearchTerm(e.target.value)}
                />
              </div>
            </div>

            {/* Country list */}
            <div className="max-h-60 overflow-auto">
              {filteredCountries.length === 0 ? (
                <div className="p-4 text-center text-gray-500">
                  No countries found
                </div>
              ) : (
                <div className="py-2">
                  {filteredCountries.map((country) => (
                    <button
                      key={country.code}
                      type="button"
                      className={`w-full text-left px-4 py-2 text-sm hover:bg-gray-100 flex items-center justify-between ${
                        selectedCountries.includes(country.code) ? 'text-brand-600 bg-brand-50' : 'text-gray-900'
                      }`}
                      onClick={() => handleCountrySelect(country.code)}
                    >
                      <span>{country.name}</span>
                      {selectedCountries.includes(country.code) && (
                        <span className="text-brand-600">✓</span>
                      )}
                    </button>
                  ))}
                </div>
              )}
            </div>
          </div>
        )}
      </div>
    </div>
  )
} 